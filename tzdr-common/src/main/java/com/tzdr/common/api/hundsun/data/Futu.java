package com.tzdr.common.api.hundsun.data;

public class Futu {
	
	private String fundAccount;
	
	private String combineId;
	
	private double futuEnableBalance;
	
	private long date;
	
	private String currencyNo;

	public String getFundAccount() {
		return fundAccount;
	}

	public void setFundAccount(String fundAccount) {
		this.fundAccount = fundAccount;
	}

	public String getCombineId() {
		return combineId;
	}

	public void setCombineId(String combineId) {
		this.combineId = combineId;
	}

	public double getFutuEnableBalance() {
		return futuEnableBalance;
	}

	public void setFutuEnableBalance(double futuEnableBalance) {
		this.futuEnableBalance = futuEnableBalance;
	}

	public long getDate() {
		return date;
	}

	public void setDate(long date) {
		this.date = date;
	}

	public String getCurrencyNo() {
		return currencyNo;
	}

	public void setCurrencyNo(String currencyNo) {
		this.currencyNo = currencyNo;
	}
	
	

}
