package com.tzdr.common.api.hundsun.data;

public class Combasset {
	
	private String fundAccount;
	
	private long assetId;
	
	private String combineId;
	
	private double assetValue;
	
	private double assetTotalValue;
	
	private double currentCash;
	
	private double stockAsset;
	
	private double fundAsset;
	
	private double bondAsset;
	
	private double hgAsset;
	
	private double futuresAsset;

	public String getFundAccount() {
		return fundAccount;
	}

	public void setFundAccount(String fundAccount) {
		this.fundAccount = fundAccount;
	}

	public long getAssetId() {
		return assetId;
	}

	public void setAssetId(long assetId) {
		this.assetId = assetId;
	}

	public String getCombineId() {
		return combineId;
	}

	public void setCombineId(String combineId) {
		this.combineId = combineId;
	}

	public double getAssetValue() {
		return assetValue;
	}

	public void setAssetValue(double assetValue) {
		this.assetValue = assetValue;
	}

	public double getAssetTotalValue() {
		return assetTotalValue;
	}

	public void setAssetTotalValue(double assetTotalValue) {
		this.assetTotalValue = assetTotalValue;
	}

	public double getCurrentCash() {
		return currentCash;
	}

	public void setCurrentCash(double currentCash) {
		this.currentCash = currentCash;
	}

	public double getStockAsset() {
		return stockAsset;
	}

	public void setStockAsset(double stockAsset) {
		this.stockAsset = stockAsset;
	}

	public double getFundAsset() {
		return fundAsset;
	}

	public void setFundAsset(double fundAsset) {
		this.fundAsset = fundAsset;
	}

	public double getBondAsset() {
		return bondAsset;
	}

	public void setBondAsset(double bondAsset) {
		this.bondAsset = bondAsset;
	}

	public double getHgAsset() {
		return hgAsset;
	}

	public void setHgAsset(double hgAsset) {
		this.hgAsset = hgAsset;
	}

	public double getFuturesAsset() {
		return futuresAsset;
	}

	public void setFuturesAsset(double futuresAsset) {
		this.futuresAsset = futuresAsset;
	}
	
	

}
